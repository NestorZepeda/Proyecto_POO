    private void btnTerminarComActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTerminarComActionPerformed
        // Boton que termina la compra añadiendo a la lista clientes y a la lista producto
        Date fecha= new Date();
        if (lClie.isEmpty()) 
        {
            per=0;
        }else
            {
                per=lClie.size();
            }
        for (int i = 0; i < 1; i++) 
        {
            lClie.add(new Cliente(String.valueOf("0"+per), txtNCliente.getText(), txtDUI.getText(), fecha, total));
            lClie.get(per).lPedido= new ArrayList<>();
            for (int j = 0; j < jtDetalleComp.getRowCount(); j++) 
            
            {
                  String prod;
                  int cont=-1;
                  Boolean recorrer=true;
                  prod=jtDetalleComp.getValueAt(j, 1).toString();
                while(recorrer)
                {
                    cont++;
                    if (Inicio.lInv.get(cont).getProducName().equals(prod)) recorrer=false;
                }
              
                String idProd=jtDetalleComp.getValueAt(j, 0).toString();
                String Producto=jtDetalleComp.getValueAt(j, 1).toString();
                int cantidad=Integer.parseInt((String)jtDetalleComp.getValueAt(j, 2));
                double precio=Double.parseDouble((String)jtDetalleComp.getValueAt(j, 3));
                double subtotal=Double.parseDouble((String)jtDetalleComp.getValueAt(j, 4));
                int existencia=Inicio.lInv.get(cont).getExistencias()-cantidad;
                Inicio.lInv.get(cont).setExistencias(existencia);
                lClie.get(per).lPedido.add(new Producto(idProd,Producto,cantidad,precio,subtotal));
            }
        }
        total=0;
        modeloventa.getDataVector().removeAllElements();
        txtNCliente.setText("");
        txtDUI.setText("");
        Spcantidad.setValue(1);
        
    }//GEN-LAST:event_btnTerminarComActionPerformed

