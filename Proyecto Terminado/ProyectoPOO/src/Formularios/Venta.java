    class funcion implements Funciones
     {

        @Override
        public double SubTotal(int Cantidad, double precio)
        {
            double sub= Cantidad*precio;
             return sub;
        }

        @Override
        public double Total( double subt, double TT)
        {
           TT+=subt;
           return TT;
        }

        @Override
        public double iva(double precio) 
        {
            return precio*0.13;
        }
            
     }
    
            private void cargaraProductos()
            {
              
                if (Inicio.lInv.isEmpty()) 
                {
                     Icon icono= new ImageIcon("src\\imagenes\\iconE.png");
                     JOptionPane.showMessageDialog(rootPane,"No hay productos que cargar","Lista Vacia",JOptionPane.PLAIN_MESSAGE,icono);
                
                }else{
                     this.CbProducto.removeAllItems();
                for (int i = 0; i < Inicio.lInv.size(); i++) 
                
                {
                    this.CbProducto.addItem(Inicio.lInv.get(i).getProducName());
                }
                }
            }

